     * @return \Illuminate\Http\Response
     */
    public function updateStaff(Request $request, User $user)
    {
        if(isset($request->staff) && !is_null($request->staff))
            $user->is_staff = true;
        else    $user->is_staff = false;
        $user->save();

        return redirect()->back()->with('success', 'User is successfully '.isset($request->staff) && !is_null($request->staff)?'staffed':'unstaffed');
    }
